# Arithmetic operations
# addition
print(10 + 21)              # 31

# subtraction
print(21 - 10)              # 11

# multiplication
print(21 * 10)              # 210

# exponentiation
print(10 ** 2)              # 100

# division to float
print(21 / 10)              # 2.1

# division to integer
print(21 // 10)             # 2

# modulus (remainder)
print(5 % 4)                # 1

