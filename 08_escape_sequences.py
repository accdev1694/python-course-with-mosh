# Escape characters

# use single quotes to add a quote inside our string

# \"        for escape double quotes
course = "python's cousre"
print(course)                       # python's cousre

# \'        for escape single quotes
course = "python\"s course"
print(course)                       # python"s course


# \\        for escape backslash
course = "python \\  course"
print(course)                       # python \  course


# \n        for escape n for new line
course = "python \n course"
print(course)                       # python
                                    # course
