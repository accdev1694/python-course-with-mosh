# ITERABLES

# this refers to elements that can be used in a loop
# lists, strings, range

# lists
nums = [21, 12, 200, 15]
for num in nums:
    print(num)
    
# strings
string = "I love pizza"
for s in string:
    print(s)
    
# range
for x in range(5):
    print(x)
    
# there are a host of other iterables in python